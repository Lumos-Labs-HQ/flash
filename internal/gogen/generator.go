package gogen

import (
	"crypto/sha256"
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/Lumos-Labs-HQ/flash/internal/config"
	"github.com/Lumos-Labs-HQ/flash/internal/gencommon"
	"github.com/Lumos-Labs-HQ/flash/internal/parser"
	"github.com/Lumos-Labs-HQ/flash/internal/utils"
)

type Generator struct {
	Config       *config.Config
	schema       *parser.Schema
	schemaParser *parser.SchemaParser
	queryParser  *parser.QueryParser
	cache        *gencommon.GenerationCache
	enumTypeMap  map[string]string // MySQL inline ENUM type -> generated enum name
}

func New(cfg *config.Config) *Generator {
	cache := gencommon.NewGenerationCache()
	cache.OutDir = cfg.Gen.Go.Out
	return &Generator{
		Config:       cfg,
		schemaParser: parser.NewSchemaParser(cfg),
		queryParser:  parser.NewQueryParser(cfg),
		cache:        cache,
	}
}

func (g *Generator) Generate() error {
	if err := os.MkdirAll(g.Config.Gen.Go.Out, 0755); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	// Parse schema
	schema, err := g.schemaParser.Parse()
	if err != nil {
		return fmt.Errorf("failed to parse schema: %w", err)
	}
	g.schema = schema
	g.enumTypeMap = g.extractInlineEnums()

	// Compute current checksums for incremental generation
	schemaHash, err := g.cache.ComputeSchemaChecksum(g.Config.SchemaDir)
	if err != nil {
		schemaHash = "" // Fallback: regenerate all
	}

	configHash := g.computeConfigChecksum()

	// Check if full regeneration is needed
	fullRegen := g.cache.ShouldRegenerateAll(schemaHash, configHash)

	// Parse queries
	queries, err := g.queryParser.Parse(schema)
	if err != nil {
		return fmt.Errorf("failed to parse queries: %w", err)
	}

	// Generate models (only if schema changed)
	if fullRegen || g.cache.SchemaChecksum != schemaHash {
		if err := g.generateModels(); err != nil {
			return err
		}
	}

	// Generate DB interface (only if needed)
	if fullRegen || g.cache.SchemaChecksum != schemaHash {
		if err := g.generateDB(); err != nil {
			return err
		}
	}

	// Generate queries with incremental support
	if err := g.generateQueriesIncremental(queries, fullRegen); err != nil {
		return err
	}

	// Update cache
	g.cache.UpdateSchemaChecksum(schemaHash)
	g.cache.UpdateConfigChecksum(configHash)
	g.cache.MarkGeneration()

	// Save cache to disk
	if err := g.cache.Save(); err != nil {
		// Non-fatal: just log and continue
		fmt.Printf("Warning: failed to save generation cache: %v\n", err)
	}

	return nil
}

// computeConfigChecksum computes hash of relevant config fields
func (g *Generator) computeConfigChecksum() string {
	// Hash relevant config fields that affect generation
	configStr := fmt.Sprintf("%s|%s|%s|%v|%v|%v|%s",
		g.Config.SchemaDir,
		g.Config.Queries,
		g.Config.Database.Provider,
		g.Config.Gen.Go.Enabled,
		g.Config.Gen.JS.Enabled,
		g.Config.Gen.Python.Enabled,
		g.Config.Gen.Go.Driver,
	)
	hash := sha256.Sum256([]byte(configStr))
	return fmt.Sprintf("%x", hash)
}

func (g *Generator) generateModels() error {
	// Ensure inline MySQL ENUMs are extracted (idempotent if already done)
	if g.enumTypeMap == nil {
		g.enumTypeMap = g.extractInlineEnums()
	}
	enumTypeMap := g.enumTypeMap

	needsTime := false
	needsSQL := false

	for _, table := range g.schema.Tables {
		for _, col := range table.Columns {
			goType := g.mapSQLTypeToGoWithEnumMap(col.Type, col.Nullable, enumTypeMap)
			if strings.Contains(goType, "time.Time") {
				needsTime = true
			}
			if strings.Contains(goType, "sql.Null") {
				needsSQL = true
			}
		}
	}

	// Pre-allocate: ~200 bytes header + ~150 bytes per table + ~100 per enum
	estimatedSize := 200 + (len(g.schema.Tables) * 150) + (len(g.schema.Enums) * 100)
	var code strings.Builder
	code.Grow(estimatedSize)

	code.WriteString("// Code generated by FlashORM. DO NOT EDIT.\n\n")
	code.WriteString(fmt.Sprintf("package %s\n\n", filepath.Base(g.Config.Gen.Go.Out)))

	// Only add imports if needed
	if needsTime || needsSQL {
		code.WriteString("import (\n")
		if needsSQL {
			code.WriteString("\t\"database/sql\"\n")
		}
		if needsTime {
			code.WriteString("\t\"time\"\n")
		}
		code.WriteString(")\n\n")
	}

	for _, enum := range g.schema.Enums {
		enumName := utils.ToPascalCase(enum.Name)
		code.WriteString(fmt.Sprintf("type %s string\n\n", enumName))
		code.WriteString("const (\n")
		for _, val := range enum.Values {
			constName := fmt.Sprintf("%s%s", enumName, utils.ToPascalCase(val))
			code.WriteString(fmt.Sprintf("\t%s %s = \"%s\"\n", constName, enumName, val))
		}
		code.WriteString(")\n\n")
	}

	// Generate CQL UDT structs (ScyllaDB/Cassandra)
	isScylla := g.Config.Database.Provider == "scylla" || g.Config.Database.Provider == "scylladb" || g.Config.Database.Provider == "cassandra"
	if isScylla && g.schema != nil {
		for _, udt := range g.schema.UDTs {
			// Use bare name (without keyspace prefix) for struct naming
			bareName := udt.Name
			if dotIdx := strings.LastIndex(udt.Name, "."); dotIdx >= 0 {
				bareName = udt.Name[dotIdx+1:]
			}
			structName := utils.ToPascalCase(bareName)
			code.WriteString(fmt.Sprintf("type %s struct {\n", structName))
			for _, f := range udt.Fields {
				fieldName := utils.ToPascalCase(f.Name)
				goType := g.mapSQLTypeToGoWithEnumMap(f.Type, false, enumTypeMap)
				code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", fieldName, goType, utils.ToSnakeCase(f.Name)))
			}
			code.WriteString("}\n\n")
		}
	}

	for _, table := range g.schema.Tables {
		structName := utils.ToPascalCase(table.Name)
		code.WriteString(fmt.Sprintf("type %s struct {\n", structName))

		// Resolve wildcard SELECT * views against the source table
		cols := table.Columns
		if len(cols) == 1 && cols[0].Name == "*" {
			cols = g.resolveWildcardViewColumns(table.Name)
		}

		for _, col := range cols {
			// Skip wildcard placeholders and view marker columns
			if col.Name == "*" || strings.HasPrefix(col.Name, "/*") {
				continue
			}
			fieldName := utils.ToPascalCase(col.Name)
			goType := g.mapSQLTypeToGoWithEnumMap(col.Type, col.Nullable, enumTypeMap)
			jsonTag := utils.ToSnakeCase(col.Name)
			code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\" db:\"%s\"`\n", fieldName, goType, jsonTag, col.Name))
		}
		code.WriteString("}\n\n")
	}

	modelsPath := filepath.Join(g.Config.Gen.Go.Out, "models.go")
	return os.WriteFile(modelsPath, []byte(code.String()), 0644)
}

func (g *Generator) generateDB() error {
	var code strings.Builder
	code.Grow(500)

	code.WriteString("// Code generated by FlashORM. DO NOT EDIT.\n\n")
	code.WriteString(fmt.Sprintf("package %s\n\n", filepath.Base(g.Config.Gen.Go.Out)))

	provider := g.Config.Database.Provider
	isScylla := provider == "scylla" || provider == "scylladb" || provider == "cassandra"

	if isScylla {
		code.WriteString("import (\n")
		code.WriteString("\t\"context\"\n\n")
		code.WriteString("\t\"github.com/apache/cassandra-gocql-driver/v2\"\n")
		code.WriteString(")\n\n")
		code.WriteString("type DBTX interface {\n")
		code.WriteString("\tQuery(stmt string, values ...interface{}) *gocql.Query\n")
		code.WriteString("}\n\n")
		code.WriteString("func Newq(db DBTX) *Queries {\n")
		code.WriteString("\treturn &Queries{db: db}\n")
		code.WriteString("}\n\n")
		code.WriteString("type Queries struct {\n")
		code.WriteString("\tdb DBTX\n")
		code.WriteString("}\n\n")
		code.WriteString("func (q *Queries) exec(ctx context.Context, query string, args ...interface{}) error {\n")
		code.WriteString("\treturn q.db.Query(query, args...).ExecContext(ctx)\n")
		code.WriteString("}\n\n")
	} else {
		isPGX := g.Config.Gen.Go.Driver == "pgx"
		if isPGX {
			code.WriteString("import (\n")
			code.WriteString("\t\"context\"\n\n")
			code.WriteString("\t\"github.com/jackc/pgx/v5\"\n")
			code.WriteString("\t\"github.com/jackc/pgx/v5/pgconn\"\n")
			code.WriteString(")\n\n")
			code.WriteString("type DBTX interface {\n")
			code.WriteString("\tExec(ctx context.Context, sql string, arguments ...interface{}) (pgconn.CommandTag, error)\n")
			code.WriteString("\tQuery(ctx context.Context, sql string, args ...interface{}) (pgx.Rows, error)\n")
			code.WriteString("\tQueryRow(ctx context.Context, sql string, args ...interface{}) pgx.Row\n")
			code.WriteString("}\n\n")
			code.WriteString("func Newq(db DBTX) *Queries {\n")
			code.WriteString("\treturn &Queries{db: db}\n")
			code.WriteString("}\n\n")
			code.WriteString("type Queries struct {\n")
			code.WriteString("\tdb DBTX\n")
			code.WriteString("}\n\n")
		} else {
			code.WriteString("import (\n")
			code.WriteString("\t\"database/sql\"\n")
			code.WriteString(")\n\n")
			code.WriteString("type DBTX interface {\n")
			code.WriteString("\tExec(query string, args ...interface{}) (sql.Result, error)\n")
			code.WriteString("\tPrepare(query string) (*sql.Stmt, error)\n")
			code.WriteString("\tQuery(query string, args ...interface{}) (*sql.Rows, error)\n")
			code.WriteString("\tQueryRow(query string, args ...interface{}) *sql.Row\n")
			code.WriteString("}\n\n")
			code.WriteString("func Newq(db DBTX) *Queries {\n")
			code.WriteString("\treturn &Queries{\n")
			code.WriteString("\t\tdb:    db,\n")
			code.WriteString("\t\tstmts: make(map[string]*sql.Stmt),\n")
			code.WriteString("\t}\n")
			code.WriteString("}\n\n")
			code.WriteString("type Queries struct {\n")
			code.WriteString("\tdb    DBTX\n")
			code.WriteString("\tstmts map[string]*sql.Stmt\n")
			code.WriteString("}\n\n")
			code.WriteString("func (q *Queries) Close() error {\n")
			code.WriteString("\tfor _, stmt := range q.stmts {\n")
			code.WriteString("\t\tif err := stmt.Close(); err != nil {\n")
			code.WriteString("\t\t\treturn err\n")
			code.WriteString("\t\t}\n")
			code.WriteString("\t}\n")
			code.WriteString("\tq.stmts = make(map[string]*sql.Stmt)\n")
			code.WriteString("\treturn nil\n")
			code.WriteString("}\n\n")
		}
	}

	dbPath := filepath.Join(g.Config.Gen.Go.Out, "db.go")
	return os.WriteFile(dbPath, []byte(code.String()), 0644)
}

func (g *Generator) expandWildcardColumns(query *parser.Query) []*parser.QueryColumn {
	hasReturning := strings.Contains(strings.ToUpper(query.SQL), "RETURNING")

	// No columns and no RETURNING clause - nothing to expand
	if len(query.Columns) == 0 && !hasReturning {
		return query.Columns
	}

	// Single non-wildcard column - no expansion needed
	if len(query.Columns) == 1 && query.Columns[0].Name != "*" {
		return query.Columns
	}

	// Multiple columns - check if any is a wildcard
	if len(query.Columns) > 1 {
		hasWildcard := false
		for _, col := range query.Columns {
			if col.Name == "*" {
				hasWildcard = true
				break
			}
		}
		if !hasWildcard {
			return query.Columns
		}
	}

	tableName := utils.ExtractTableName(query.SQL)
	if tableName == "" {
		return query.Columns
	}

	var table *parser.Table
	for _, t := range g.schema.Tables {
		if strings.EqualFold(t.Name, tableName) {
			table = t
			break
		}
	}

	if table == nil {
		return query.Columns
	}

	// If the table only has a wildcard column (SELECT * view with unresolved source),
	// try to find the source table from the view's stored SQL.
	cols := table.Columns
	if len(cols) == 1 && cols[0].Name == "*" {
		// The view column's Type holds the raw view SQL — extract the FROM table
		sourceSQL := cols[0].Type
		sourceTable := utils.ExtractTableName(sourceSQL)
		if sourceTable != "" {
			for _, t := range g.schema.Tables {
				if strings.EqualFold(t.Name, sourceTable) && !(len(t.Columns) == 1 && t.Columns[0].Name == "*") {
					cols = t.Columns
					break
				}
			}
		}
		// Still unresolved — return empty so the method generates with no columns
		if len(cols) == 1 && cols[0].Name == "*" {
			return []*parser.QueryColumn{}
		}
	}

	expanded := make([]*parser.QueryColumn, 0, len(cols))
	for _, col := range cols {
		if col.Name == "*" || strings.HasPrefix(col.Name, "/*") {
			continue
		}
		expanded = append(expanded, &parser.QueryColumn{
			Name:     col.Name,
			Type:     col.Type,
			Table:    table.Name,
			Nullable: col.Nullable,
		})
	}

	return expanded
}

func (g *Generator) generateQueryMethod(code *strings.Builder, query *parser.Query) error {
	provider := g.Config.Database.Provider
	isScylla := provider == "scylla" || provider == "scylladb" || provider == "cassandra"
	if isScylla {
		return g.generateGocqlQueryMethod(code, query)
	}
	if g.Config.Gen.Go.Driver == "pgx" {
		return g.generatePGXQueryMethod(code, query)
	}
	return g.generateSQLQueryMethod(code, query)
}

func (g *Generator) generateSQLQueryMethod(code *strings.Builder, query *parser.Query) error {
	columns := g.expandWildcardColumns(query)

	methodName := utils.ToPascalCase(query.Name)

	useStructParams := len(query.Params) > 3

	if useStructParams && len(query.Params) > 0 {
		code.WriteString(fmt.Sprintf("type %sParams struct {\n", methodName))
		for _, param := range query.Params {
			fieldName := utils.ToPascalCase(param.Name)
			goType := g.mapParamTypeToGo(param.Type)
			code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", fieldName, goType, utils.SafeGoIdent(utils.ToSnakeCase(param.Name))))
		}
		code.WriteString("}\n\n")
	}

	cmd := strings.ToLower(query.Cmd)
	isModifying := utils.IsModifyingQuery(query.SQL)

	code.WriteString(fmt.Sprintf("func (q *Queries) %s(", methodName))

	if len(query.Params) > 0 {
		if useStructParams {
			code.WriteString(fmt.Sprintf("arg %sParams", methodName))
		} else {
			paramList := []string{}
			for _, param := range query.Params {
				paramName := utils.SafeGoIdent(utils.ToSnakeCase(param.Name))
				goType := g.mapParamTypeToGo(param.Type)
				paramList = append(paramList, fmt.Sprintf("%s %s", paramName, goType))
			}
			code.WriteString(strings.Join(paramList, ", "))
		}
	}

	code.WriteString(") (")

	switch {
	case cmd == ":one":
		if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("%s, error", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
		} else {
			code.WriteString(fmt.Sprintf("%sRow, error", methodName))
		}
	case cmd == ":many":
		if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("[]%s, error", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
		} else {
			code.WriteString(fmt.Sprintf("[]%sRow, error", methodName))
		}
	case cmd == ":exec" || isModifying:
		code.WriteString("error")
	case cmd == ":execresult":
		code.WriteString("sql.Result, error")
	default:
		code.WriteString("error")
	}

	code.WriteString(") {\n")

	cleanSQL := strings.TrimSpace(query.SQL)
	// Handle backticks in SQL by splitting the raw string literal
	if strings.Contains(cleanSQL, "`") {
		parts := strings.Split(cleanSQL, "`")
		code.WriteString("\tconst query = `")
		for i, part := range parts {
			if i > 0 {
				code.WriteString("` + \"`\" + `")
			}
			code.WriteString(part)
		}
		code.WriteString("`\n")
	} else {
		code.WriteString(fmt.Sprintf("\tconst query = `%s`\n", cleanSQL))
	}

	isHotQuery := len(query.Params) <= 3 && !strings.Contains(strings.ToUpper(query.SQL), "UNION")
	stmtKey := fmt.Sprintf("%s_stmt", methodName)

	if isHotQuery {
		code.WriteString(fmt.Sprintf("\tstmt := q.stmts[\"%s\"]\n", stmtKey))
		code.WriteString("\tif stmt == nil {\n")
		code.WriteString("\t\tvar err error\n")
		code.WriteString("\t\tstmt, err = q.db.Prepare(query)\n")
		code.WriteString("\t\tif err != nil {\n")

		// Return appropriate zero value based on return type
		if len(columns) > 0 {
			// Query returns data (SELECT or INSERT RETURNING)
			if cmd == ":one" {
				if len(columns) == 1 {
					code.WriteString("\t\t\treturn ")
					code.WriteString(g.getZeroValue(g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
					code.WriteString(", err\n")
				} else {
					code.WriteString(fmt.Sprintf("\t\t\treturn %sRow{}, err\n", methodName))
				}
			} else {
				// :many or default (returns slice)
				code.WriteString("\t\t\treturn nil, err\n")
			}
		} else if cmd == ":execresult" {
			code.WriteString("\t\t\treturn nil, err\n")
		} else {
			// :exec or modifying query without RETURNING
			code.WriteString("\t\t\treturn err\n")
		}

		code.WriteString("\t\t}\n")
		code.WriteString(fmt.Sprintf("\t\tq.stmts[\"%s\"] = stmt\n", stmtKey))
		code.WriteString("\t}\n")
	}

	if len(query.Params) > 0 {
		code.WriteString("\targs := []interface{}{")
		for i, param := range query.Params {
			if i > 0 {
				code.WriteString(", ")
			}
			if useStructParams {
				code.WriteString(fmt.Sprintf("arg.%s", utils.ToPascalCase(param.Name)))
			} else {
				code.WriteString(utils.SafeGoIdent(utils.ToSnakeCase(param.Name)))
			}
		}
		code.WriteString("}\n")
	}

	switch {
	case cmd == ":one":
		if len(columns) == 0 {
			code.WriteString("\n\treturn nil, fmt.Errorf(\"query has no return columns\")\n")
		} else if len(columns) > 1 {
			code.WriteString(fmt.Sprintf("\n\tvar result %sRow\n", methodName))
			if isHotQuery {
				code.WriteString("\trows, err := stmt.Query(")
				if len(query.Params) > 0 {
					code.WriteString("args...")
				}
			} else {
				code.WriteString("\trows, err := q.db.Query(query")
				if len(query.Params) > 0 {
					code.WriteString(", args...")
				}
			}
			code.WriteString(")\n")
			code.WriteString("\tif err != nil {\n")
			code.WriteString("\t\treturn result, err\n")
			code.WriteString("\t}\n")
			code.WriteString("\tdefer rows.Close()\n")
			code.WriteString("\tif !rows.Next() {\n")
			code.WriteString("\t\tif err := rows.Err(); err != nil {\n")
			code.WriteString("\t\t\treturn result, err\n")
			code.WriteString("\t\t}\n")
			code.WriteString("\t\treturn result, sql.ErrNoRows\n")
			code.WriteString("\t}\n")
			code.WriteString("\terr = rows.Scan(")
			for i, col := range columns {
				if i > 0 {
					code.WriteString(", ")
				}
				code.WriteString(fmt.Sprintf("&result.%s", utils.ToPascalCase(col.Name)))
			}
			code.WriteString(")\n")
			code.WriteString("\treturn result, err\n")
		} else {
			code.WriteString("\n\tvar result ")
			code.WriteString(g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable))
			if isHotQuery {
				code.WriteString("\n\trows, err := stmt.Query(")
				if len(query.Params) > 0 {
					code.WriteString("args...")
				}
			} else {
				code.WriteString("\n\trows, err := q.db.Query(query")
				if len(query.Params) > 0 {
					code.WriteString(", args...")
				}
			}
			code.WriteString(")\n")
			code.WriteString("\tif err != nil {\n")
			code.WriteString("\t\treturn result, err\n")
			code.WriteString("\t}\n")
			code.WriteString("\tdefer rows.Close()\n")
			code.WriteString("\tif !rows.Next() {\n")
			code.WriteString("\t\tif err := rows.Err(); err != nil {\n")
			code.WriteString("\t\t\treturn result, err\n")
			code.WriteString("\t\t}\n")
			code.WriteString("\t\treturn result, sql.ErrNoRows\n")
			code.WriteString("\t}\n")
			code.WriteString("\terr = rows.Scan(&result)\n")
			code.WriteString("\treturn result, err\n")
		}

	case cmd == ":many":
		if len(columns) == 0 {
			code.WriteString("\n\treturn nil, fmt.Errorf(\"query has no return columns\")\n")
		} else {
			if isHotQuery {
				code.WriteString("\n\trows, err := stmt.Query(")
				if len(query.Params) > 0 {
					code.WriteString("args...")
				}
			} else {
				code.WriteString("\n\trows, err := q.db.Query(query")
				if len(query.Params) > 0 {
					code.WriteString(", args...")
				}
			}
			code.WriteString(")\n")
			code.WriteString("\tif err != nil {\n")
			code.WriteString("\t\treturn nil, err\n")
			code.WriteString("\t}\n")
			code.WriteString("\tdefer rows.Close()\n\n")

			if len(columns) > 1 {
				code.WriteString(fmt.Sprintf("\titems := make([]%sRow, 0, 8) \n", methodName))
			} else {
				code.WriteString(fmt.Sprintf("\titems := make([]%s, 0, 8) \n", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
			}

			code.WriteString("\tfor rows.Next() {\n")
			if len(columns) > 1 {
				code.WriteString(fmt.Sprintf("\t\tvar item %sRow\n", methodName))
				code.WriteString("\t\tif err := rows.Scan(")
				for i, col := range columns {
					if i > 0 {
						code.WriteString(", ")
					}
					code.WriteString(fmt.Sprintf("&item.%s", utils.ToPascalCase(col.Name)))
				}
				code.WriteString("); err != nil {\n")
				code.WriteString("\t\t\treturn nil, err\n")
				code.WriteString("\t\t}\n")
				code.WriteString("\t\titems = append(items, item)\n")
			} else {
				code.WriteString("\t\tvar item ")
				code.WriteString(g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable))
				code.WriteString("\n\t\tif err := rows.Scan(&item); err != nil {\n")
				code.WriteString("\t\t\treturn nil, err\n")
				code.WriteString("\t\t}\n")
				code.WriteString("\t\titems = append(items, item)\n")
			}
			code.WriteString("\t}\n")
			code.WriteString("\treturn items, rows.Err()\n")
		}

	case cmd == ":execresult":
		if isHotQuery {
			code.WriteString("\n\treturn stmt.Exec(")
			if len(query.Params) > 0 {
				code.WriteString("args...")
			}
		} else {
			code.WriteString("\n\treturn q.db.Exec(query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
		}
		code.WriteString(")\n")

	default:
		if isHotQuery {
			code.WriteString("\n\t_, err := stmt.Exec(")
			if len(query.Params) > 0 {
				code.WriteString("args...")
			}
		} else {
			code.WriteString("\n\t_, err := q.db.Exec(query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
		}
		code.WriteString(")\n")
		code.WriteString("\treturn err\n")
	}

	code.WriteString("}\n\n")

	if (cmd == ":one" || cmd == ":many") && len(columns) > 1 {
		code.WriteString(fmt.Sprintf("type %sRow struct {\n", methodName))
		for _, col := range columns {
			fieldName := utils.ToPascalCase(col.Name)
			// Gocql Row types use value types not pointers (assigned via fmt.Sprint)
			goType := g.mapSQLTypeToGo(col.Type, false)
			code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", fieldName, goType, utils.ToSnakeCase(col.Name)))
		}
		code.WriteString("}\n\n")
	}

	return nil
}

func (g *Generator) mapSQLTypeToGo(sqlType string, nullable bool) string {
	sqlTypeLower := strings.ToLower(sqlType)

	provider := g.Config.Database.Provider
	isScylla := provider == "scylla" || provider == "scylladb" || provider == "cassandra"

	if g.schema != nil {
		for _, enum := range g.schema.Enums {
			if strings.ToLower(enum.Name) == sqlTypeLower {
				enumType := utils.ToPascalCase(enum.Name)
				if nullable {
					if isScylla {
						return "*" + enumType
					}
					return "sql.NullString"
				}
				return enumType
			}
		}
	}

	if strings.HasPrefix(sqlTypeLower, "enum(") {
		if nullable {
			if isScylla {
				return "*string"
			}
			return "sql.NullString"
		}
		return "string"
	}

	baseType := ""
	switch {
	// ScyllaDB/Cassandra CQL types — check before generic contains-based cases
	case sqlTypeLower == "timeuuid":
		baseType = "string" // timeuuid is a UUID type, not a time — though gocql can scan it as time.Time, string is safer
	case sqlTypeLower == "uuid":
		baseType = "string"
	case sqlTypeLower == "inet":
		baseType = "string"
	case sqlTypeLower == "varint":
		baseType = "int64"
	case sqlTypeLower == "counter":
		baseType = "int64"
	case sqlTypeLower == "duration":
		baseType = "int64"
	// ScyllaDB/Cassandra collection types — must be before generic string/int checks
	case strings.HasPrefix(sqlTypeLower, "set<"), strings.HasPrefix(sqlTypeLower, "list<"):
		// Extract element type and map to []GoType
		inner := extractCollectionInner(sqlTypeLower)
		elemGo := g.mapSQLTypeToGo(inner, false)
		if nullable && isScylla {
			return "*[]" + elemGo
		}
		return "[]" + elemGo
	case strings.HasPrefix(sqlTypeLower, "map<"):
		// map<key,value> → map[KeyType]ValueType
		k, v := extractMapTypes(sqlTypeLower)
		kGo := g.mapSQLTypeToGo(k, false)
		vGo := g.mapSQLTypeToGo(v, false)
		if nullable && isScylla {
			return fmt.Sprintf("*map[%s]%s", kGo, vGo)
		}
		return fmt.Sprintf("map[%s]%s", kGo, vGo)
	case strings.HasPrefix(sqlTypeLower, "frozen<"):
		// frozen<type> — unwrap and check for UDT
		inner := extractCollectionInner(sqlTypeLower)
		if isScylla && g.schema != nil {
			for _, udt := range g.schema.UDTs {
				bareName := udt.Name
				if dotIdx := strings.LastIndex(udt.Name, "."); dotIdx >= 0 {
					bareName = udt.Name[dotIdx+1:]
				}
				if strings.EqualFold(bareName, inner) || strings.EqualFold(udt.Name, inner) {
					goName := utils.ToPascalCase(bareName)
					if nullable {
						return "*" + goName
					}
					return goName
				}
			}
		}
		// frozen<tuple<...>> or other frozen types → string
		baseType = "string"
	case strings.HasPrefix(sqlTypeLower, "tuple<"):
		baseType = "string"
	case strings.Contains(sqlTypeLower, "int"):
		baseType = "int64"
	case strings.Contains(sqlTypeLower, "serial"):
		baseType = "int64"
	// PostgreSQL range types — must be before contains("int")
	case strings.HasSuffix(sqlTypeLower, "range"):
		baseType = "string" // int4range, int8range, numrange, tsrange etc. serialize as "[a,b)"
	case sqlTypeLower == "inet" || sqlTypeLower == "cidr" || sqlTypeLower == "macaddr":
		baseType = "string"
	case sqlTypeLower == "bytea":
		baseType = "[]byte"
	case strings.Contains(sqlTypeLower, "float") || strings.Contains(sqlTypeLower, "double") || strings.Contains(sqlTypeLower, "numeric") || strings.Contains(sqlTypeLower, "decimal"):
		baseType = "float64"
	case strings.Contains(sqlTypeLower, "bool"):
		baseType = "bool"
	case strings.Contains(sqlTypeLower, "time") || strings.Contains(sqlTypeLower, "date"):
		baseType = "time.Time"
	case sqlTypeLower == "interval":
		baseType = "time.Duration"
	case strings.Contains(sqlTypeLower, "jsonb") || strings.Contains(sqlTypeLower, "json"):
		if g.Config.Gen.Go.Driver == "pgx" {
			baseType = "map[string]interface{}"
		} else {
			baseType = "[]byte"
		}
	case strings.HasSuffix(sqlTypeLower, "[]"):
		// Array types: TEXT[], INTEGER[], etc.
		elemType := strings.TrimSuffix(sqlTypeLower, "[]")
		elemGo := g.mapSQLTypeToGo(elemType, false)
		return "[]" + elemGo
	case strings.Contains(sqlTypeLower, "uuid"):
		baseType = "string"
	// ClickHouse-specific types
	case sqlTypeLower == "uint8", sqlTypeLower == "uint16", sqlTypeLower == "uint32", sqlTypeLower == "uint64":
		baseType = "uint64"
	case sqlTypeLower == "int8", sqlTypeLower == "int16", sqlTypeLower == "int32", sqlTypeLower == "int64":
		baseType = "int64"
	case sqlTypeLower == "float32":
		baseType = "float32"
	case sqlTypeLower == "float64":
		baseType = "float64"
	case sqlTypeLower == "string", strings.HasPrefix(sqlTypeLower, "fixedstring"):
		baseType = "string"
	case strings.HasPrefix(sqlTypeLower, "nullable("):
		inner := sqlTypeLower[9 : len(sqlTypeLower)-1]
		return g.mapSQLTypeToGo(inner, true)
	default:
		// Check if bare CQL UDT name (not wrapped in frozen<>)
		if isScylla && g.schema != nil {
			for _, udt := range g.schema.UDTs {
				bareName := udt.Name
				if dotIdx := strings.LastIndex(udt.Name, "."); dotIdx >= 0 {
					bareName = udt.Name[dotIdx+1:]
				}
				if strings.EqualFold(bareName, sqlTypeLower) || strings.EqualFold(udt.Name, sqlTypeLower) {
					goName := utils.ToPascalCase(bareName)
					if nullable {
						return "*" + goName
					}
					return goName
				}
			}
		}
		baseType = "string"
	}
	provider = g.Config.Database.Provider
	isScylla = provider == "scylla" || provider == "scylladb" || provider == "cassandra"
	if nullable {
		if isScylla {
			// gocql uses pointer types for nullable columns, not sql.Null*
			return "*" + baseType
		}
		switch baseType {
		case "int64":
			return "sql.NullInt64"
		case "float64":
			return "sql.NullFloat64"
		case "bool":
			return "sql.NullBool"
		case "string":
			return "sql.NullString"
		case "time.Time":
			return "sql.NullTime"
		default:
			return "*" + baseType
		}
	}
	return baseType
}

func (g *Generator) mapParamTypeToGo(paramType string) string {
	if strings.Contains(paramType, "|") {
		parts := strings.Split(paramType, "|")
		paramType = strings.TrimSpace(parts[0])
	}

	if g.schema != nil {
		for _, enum := range g.schema.Enums {
			if strings.EqualFold(enum.Name, paramType) {
				return utils.ToPascalCase(enum.Name)
			}
		}
	}

	// For gocql, collection types map to []string (gocql handles the conversion)
	provider := g.Config.Database.Provider
	isScylla := provider == "scylla" || provider == "scylladb" || provider == "cassandra"
	if isScylla {
		lower := strings.ToLower(paramType)
		if strings.HasPrefix(lower, "set<") || strings.HasPrefix(lower, "list<") {
			return "[]string"
		}
	}

	return g.mapSQLTypeToGo(paramType, false)
}

func (g *Generator) mapColumnTypeToGo(colType string, nullable bool) string {
	provider := g.Config.Database.Provider
	isScylla := provider == "scylla" || provider == "scylladb" || provider == "cassandra"
	if g.enumTypeMap != nil {
		if enumName, ok := g.enumTypeMap[strings.ToLower(colType)]; ok {
			typeName := utils.ToPascalCase(enumName)
			if nullable {
				if isScylla {
					return "*" + typeName
				}
				return "sql.NullString"
			}
			return typeName
		}
	}
	return g.mapSQLTypeToGo(colType, nullable)
}

// mapSQLTypeToGoWithEnumMap maps SQL types to Go types using enum type map for MySQL inline ENUMs
func (g *Generator) mapSQLTypeToGoWithEnumMap(sqlType string, nullable bool, enumTypeMap map[string]string) string {
	sqlTypeLower := strings.ToLower(sqlType)

	if enumName, ok := enumTypeMap[sqlTypeLower]; ok {
		typeName := utils.ToPascalCase(enumName)
		if nullable {
			provider := g.Config.Database.Provider
			if provider == "scylla" || provider == "scylladb" || provider == "cassandra" {
				return "*" + typeName
			}
			return "sql.NullString"
		}
		return typeName
	}

	return g.mapSQLTypeToGo(sqlType, nullable)
}

// getZeroValue returns the zero value for a given Go type
func (g *Generator) getZeroValue(goType string) string {
	switch {
	case strings.HasPrefix(goType, "[]"):
		return "nil"
	case strings.Contains(goType, "sql.Null"):
		return goType + "{}"
	case goType == "string":
		return `""`
	case goType == "int", goType == "int32", goType == "int64", goType == "float32", goType == "float64":
		return "0"
	case goType == "bool":
		return "false"
	case strings.Contains(goType, "time.Time"):
		return "time.Time{}"
	default:
		return goType + "{}"
	}
}

// extractInlineEnums extracts MySQL inline ENUM types and adds them to schema
func (g *Generator) extractInlineEnums() map[string]string {
	enumTypeMap := make(map[string]string)

	if g.schema == nil {
		return enumTypeMap
	}

	for _, table := range g.schema.Tables {
		for _, col := range table.Columns {
			colTypeLower := strings.ToLower(col.Type)
			if strings.HasPrefix(colTypeLower, "enum(") {
				values := extractEnumValues(col.Type)
				if len(values) > 0 {
					enumName := fmt.Sprintf("%s_%s", table.Name, col.Name)

					enumTypeMap[colTypeLower] = enumName

					g.schema.Enums = append(g.schema.Enums, &parser.Enum{
						Name:   enumName,
						Values: values,
					})
				}
			}
		}
	}

	return enumTypeMap
}

// extractEnumValues extracts values from inline ENUM type
func extractEnumValues(columnType string) []string {
	columnType = strings.ToLower(columnType)
	if !strings.HasPrefix(columnType, "enum(") {
		return nil
	}

	values := strings.TrimPrefix(columnType, "enum(")
	values = strings.TrimSuffix(values, ")")

	var result []string
	parts := strings.Split(values, ",")
	for _, part := range parts {
		part = strings.TrimSpace(part)
		part = strings.Trim(part, "'\"")
		if part != "" {
			result = append(result, part)
		}
	}

	return result
}

// extractCQLInner extracts the inner type from a CQL collection wrapper like frozen<X> or list<X>.
func extractCQLInner(typ, wrapper string) (string, bool) {
	prefix := wrapper + "<"
	if strings.HasPrefix(typ, prefix) && strings.HasSuffix(typ, ">") {
		inner := typ[len(prefix) : len(typ)-1]
		return strings.TrimSpace(inner), true
	}
	return "", false
}

// extractCollectionInner extracts the element type from set<T>, list<T>, or frozen<T>.
func extractCollectionInner(typ string) string {
	for _, wrapper := range []string{"set", "list", "frozen"} {
		if inner, ok := extractCQLInner(typ, wrapper); ok {
			return inner
		}
	}
	return "text"
}

// extractMapTypes extracts key and value types from map<K,V>.
func extractMapTypes(typ string) (string, string) {
	k, v, ok := extractCQLMap(typ)
	if !ok {
		return "string", "string"
	}
	return k, v
}

// resolveWildcardViewColumns returns nil for SELECT * views — the struct will be empty
// but won't generate an invalid `* *string` field.
func (g *Generator) resolveWildcardViewColumns(viewName string) []*parser.Column {
	return nil
}

// extractCQLMap extracts key and value types from a CQL map<key,value>.
func extractCQLMap(typ string) (string, string, bool) {
	if !strings.HasPrefix(typ, "map<") || !strings.HasSuffix(typ, ">") {
		return "", "", false
	}
	inner := typ[4 : len(typ)-1]
	angle := 0
	for i, ch := range inner {
		switch ch {
		case '<':
			angle++
		case '>':
			angle--
		case ',':
			if angle == 0 {
				key := strings.TrimSpace(inner[:i])
				val := strings.TrimSpace(inner[i+1:])
				return key, val, true
			}
		}
	}
	return "", "", false
}

func (g *Generator) generateGocqlQueryMethod(code *strings.Builder, query *parser.Query) error {
	columns := g.expandWildcardColumns(query)
	methodName := utils.ToPascalCase(query.Name)
	useStructParams := len(query.Params) > 3

	cmd := strings.ToLower(query.Cmd)
	hasReturning := strings.Contains(strings.ToUpper(query.SQL), "RETURNING")

	// CQL does not support RETURNING — treat as :exec but return the inserted row
	// by re-reading params (we know what we inserted).
	if hasReturning {
		cmd = ":exec"
	}

	if useStructParams && len(query.Params) > 0 {
		code.WriteString(fmt.Sprintf("type %sParams struct {\n", methodName))
		for _, param := range query.Params {
			fieldName := utils.ToPascalCase(param.Name)
			goType := g.mapParamTypeToGo(param.Type)
			code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", fieldName, goType, utils.SafeGoIdent(utils.ToSnakeCase(param.Name))))
		}
		code.WriteString("}\n\n")
	}

	// Signature
	code.WriteString(fmt.Sprintf("func (q *Queries) %s(ctx context.Context, ", methodName))
	if len(query.Params) > 0 {
		if useStructParams {
			code.WriteString(fmt.Sprintf("arg %sParams", methodName))
		} else {
			var parts []string
			for _, param := range query.Params {
				parts = append(parts, fmt.Sprintf("%s %s", utils.SafeGoIdent(utils.ToSnakeCase(param.Name)), g.mapParamTypeToGo(param.Type)))
			}
			code.WriteString(strings.Join(parts, ", "))
		}
	}
	code.WriteString(") (")
	switch cmd {
	case ":one":
		if len(columns) == 0 {
			code.WriteString("error")
		} else if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("%s, error", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
		} else {
			code.WriteString(fmt.Sprintf("%sRow, error", methodName))
		}
	case ":many":
		if len(columns) == 0 {
			code.WriteString("error")
		} else if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("[]%s, error", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
		} else {
			code.WriteString(fmt.Sprintf("[]%sRow, error", methodName))
		}
	default:
		code.WriteString("error")
	}
	code.WriteString(") {\n")

	// SQL constant — strip RETURNING, handle BATCH for multi-statement
	cleanSQL := strings.TrimSpace(query.SQL)
	if idx := strings.LastIndex(strings.ToUpper(cleanSQL), "RETURNING"); idx >= 0 {
		cleanSQL = strings.TrimSpace(cleanSQL[:idx])
	}
	// Multi-statement → BATCH
	stmts := splitCQLStatements(cleanSQL)
	if len(stmts) > 1 {
		batch := "BEGIN BATCH\n"
		for _, s := range stmts {
			batch += "\t" + strings.TrimSpace(s) + ";\n"
		}
		batch += "APPLY BATCH"
		cleanSQL = batch
	}

	code.WriteString(fmt.Sprintf("\tconst query = `%s`\n", cleanSQL))

	// Args
	// Only declare args if we'll actually use them (not in zero-column :many fallback)
	needsArgs := len(query.Params) > 0 && !(cmd == ":many" && len(columns) == 0)
	if needsArgs {
		code.WriteString("\targs := []interface{}{")
		for i, param := range query.Params {
			if i > 0 {
				code.WriteString(", ")
			}
			if useStructParams {
				code.WriteString("arg." + utils.ToPascalCase(param.Name))
			} else {
				code.WriteString(utils.SafeGoIdent(utils.ToSnakeCase(param.Name)))
			}
		}
		code.WriteString("}\n")
	}

	// Body
	switch cmd {
	case ":one":
		if len(columns) == 0 {
			// No columns — fallback exec
			code.WriteString("\treturn q.exec(ctx, query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(")\n")
		} else if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("\n\tvar result %s\n", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
			code.WriteString("\tif err := q.db.Query(query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(").WithContext(ctx).Scan(&result); err != nil {\n")
			code.WriteString(fmt.Sprintf("\t\treturn %s, err\n", gocqlZero(g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable))))
			code.WriteString("\t}\n\treturn result, nil\n")
		} else {
			code.WriteString(fmt.Sprintf("\n\tvar result %sRow\n", methodName))
			code.WriteString("\terr := q.db.Query(query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(").WithContext(ctx).Scan(\n")
			for _, col := range columns {
				code.WriteString(fmt.Sprintf("\t\t&result.%s,\n", utils.ToPascalCase(col.Name)))
			}
			code.WriteString("\t)\n")
			code.WriteString("\tif err != nil {\n\t\treturn result, err\n\t}\n")
			code.WriteString("\treturn result, nil\n")
		}
	case ":many":
		if len(columns) == 0 {
			code.WriteString("\treturn q.exec(ctx, query)\n")
		} else if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("\n\tvar items []%s\n", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
			code.WriteString("\titer := q.db.Query(query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(").WithContext(ctx).Iter()\n")
			code.WriteString(fmt.Sprintf("\tvar v %s\n", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
			code.WriteString("\tfor iter.Scan(&v) {\n\t\titems = append(items, v)\n\t}\n")
			code.WriteString("\treturn items, iter.Close()\n")
		} else {
			code.WriteString(fmt.Sprintf("\n\tvar items []%sRow\n", methodName))
			code.WriteString("\titer := q.db.Query(query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(").WithContext(ctx).Iter()\n")
			code.WriteString(fmt.Sprintf("\tvar item %sRow\n", methodName))
			code.WriteString("\tfor iter.Scan(\n")
			for _, col := range columns {
				code.WriteString(fmt.Sprintf("\t\t&item.%s,\n", utils.ToPascalCase(col.Name)))
			}
			code.WriteString("\t) {\n\t\titems = append(items, item)\n\t}\n")
			code.WriteString("\treturn items, iter.Close()\n")
		}
	default:
		code.WriteString("\n\treturn q.exec(ctx, query")
		if len(query.Params) > 0 {
			code.WriteString(", args...")
		}
		code.WriteString(")\n")
	}
	code.WriteString("}\n\n")

	// Row struct
	if (cmd == ":one" || cmd == ":many") && len(columns) > 1 {
		code.WriteString(fmt.Sprintf("type %sRow struct {\n", methodName))
		for _, col := range columns {
			goType := g.mapSQLTypeToGo(col.Type, false)
			code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", utils.ToPascalCase(col.Name), goType, utils.ToSnakeCase(col.Name)))
		}
		code.WriteString("}\n\n")
	}

	return nil
}

// splitCQLStatements splits multiple CQL statements (separated by ;) for BATCH wrapping.
func splitCQLStatements(sql string) []string {
	var stmts []string
	for _, s := range strings.Split(sql, ";") {
		if s = strings.TrimSpace(s); s != "" {
			stmts = append(stmts, s)
		}
	}
	return stmts
}

// gocqlZero returns the zero value expression for a Go type (for early-return on error).
func gocqlZero(goType string) string {
	switch {
	case goType == "string":
		return `""`
	case goType == "bool":
		return "false"
	case strings.HasPrefix(goType, "[]") || strings.HasPrefix(goType, "map[") || strings.HasPrefix(goType, "*"):
		return "nil"
	case goType == "int64" || goType == "float64":
		return "0"
	case goType == "time.Time":
		return "time.Time{}"
	default:
		return goType + "{}"
	}
}

func (g *Generator) generatePGXQueryMethod(code *strings.Builder, query *parser.Query) error {
	columns := g.expandWildcardColumns(query)
	methodName := utils.ToPascalCase(query.Name)
	useStructParams := len(query.Params) > 3

	if useStructParams && len(query.Params) > 0 {
		code.WriteString(fmt.Sprintf("type %sParams struct {\n", methodName))
		for _, param := range query.Params {
			fieldName := utils.ToPascalCase(param.Name)
			goType := g.mapParamTypeToGo(param.Type)
			code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", fieldName, goType, utils.SafeGoIdent(utils.ToSnakeCase(param.Name))))
		}
		code.WriteString("}\n\n")
	}

	cmd := strings.ToLower(query.Cmd)
	isModifying := utils.IsModifyingQuery(query.SQL)

	code.WriteString(fmt.Sprintf("func (q *Queries) %s(ctx context.Context, ", methodName))

	if len(query.Params) > 0 {
		if useStructParams {
			code.WriteString(fmt.Sprintf("arg %sParams", methodName))
		} else {
			paramList := []string{}
			for _, param := range query.Params {
				paramName := utils.SafeGoIdent(utils.ToSnakeCase(param.Name))
				goType := g.mapParamTypeToGo(param.Type)
				paramList = append(paramList, fmt.Sprintf("%s %s", paramName, goType))
			}
			code.WriteString(strings.Join(paramList, ", "))
		}
	}

	code.WriteString(") (")

	switch {
	case cmd == ":one":
		if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("%s, error", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
		} else {
			code.WriteString(fmt.Sprintf("%sRow, error", methodName))
		}
	case cmd == ":many":
		if len(columns) == 1 {
			code.WriteString(fmt.Sprintf("[]%s, error", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
		} else {
			code.WriteString(fmt.Sprintf("[]%sRow, error", methodName))
		}
	case cmd == ":exec" || isModifying:
		code.WriteString("error")
	case cmd == ":execresult":
		code.WriteString("(pgconn.CommandTag, error)")
	default:
		code.WriteString("error")
	}

	code.WriteString(") {\n")

	cleanSQL := strings.TrimSpace(query.SQL)
	if strings.Contains(cleanSQL, "`") {
		parts := strings.Split(cleanSQL, "`")
		code.WriteString("\tconst query = `")
		for i, part := range parts {
			if i > 0 {
				code.WriteString("` + \"`\" + `")
			}
			code.WriteString(part)
		}
		code.WriteString("`\n")
	} else {
		code.WriteString(fmt.Sprintf("\tconst query = `%s`\n", cleanSQL))
	}

	if len(query.Params) > 0 {
		code.WriteString("\targs := []interface{}{")
		for i, param := range query.Params {
			if i > 0 {
				code.WriteString(", ")
			}
			if useStructParams {
				code.WriteString(fmt.Sprintf("arg.%s", utils.ToPascalCase(param.Name)))
			} else {
				code.WriteString(utils.SafeGoIdent(utils.ToSnakeCase(param.Name)))
			}
		}
		code.WriteString("}\n")
	}

	switch {
	case cmd == ":one":
		if len(columns) == 0 {
			code.WriteString("\n\treturn nil, fmt.Errorf(\"query has no return columns\")\n")
		} else if len(columns) > 1 {
			code.WriteString(fmt.Sprintf("\n\tvar result %sRow\n", methodName))
			code.WriteString("\trow := q.db.QueryRow(ctx, query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(")\n")
			code.WriteString("\terr := row.Scan(")
			for i, col := range columns {
				if i > 0 {
					code.WriteString(", ")
				}
				code.WriteString(fmt.Sprintf("&result.%s", utils.ToPascalCase(col.Name)))
			}
			code.WriteString(")\n")
			code.WriteString("\treturn result, err\n")
		} else {
			code.WriteString("\n\tvar result ")
			code.WriteString(g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable))
			code.WriteString("\n\trow := q.db.QueryRow(ctx, query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(")\n")
			code.WriteString("\terr := row.Scan(&result)\n")
			code.WriteString("\treturn result, err\n")
		}

	case cmd == ":many":
		if len(columns) == 0 {
			code.WriteString("\n\treturn nil, fmt.Errorf(\"query has no return columns\")\n")
		} else {
			code.WriteString("\n\trows, err := q.db.Query(ctx, query")
			if len(query.Params) > 0 {
				code.WriteString(", args...")
			}
			code.WriteString(")\n")
			code.WriteString("\tif err != nil {\n")
			code.WriteString("\t\treturn nil, err\n")
			code.WriteString("\t}\n")
			code.WriteString("\tdefer rows.Close()\n\n")

			if len(columns) > 1 {
				code.WriteString(fmt.Sprintf("\titems := make([]%sRow, 0, 8) \n", methodName))
			} else {
				code.WriteString(fmt.Sprintf("\titems := make([]%s, 0, 8) \n", g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable)))
			}

			code.WriteString("\tfor rows.Next() {\n")
			if len(columns) > 1 {
				code.WriteString(fmt.Sprintf("\t\tvar item %sRow\n", methodName))
				code.WriteString("\t\tif err := rows.Scan(")
				for i, col := range columns {
					if i > 0 {
						code.WriteString(", ")
					}
					code.WriteString(fmt.Sprintf("&item.%s", utils.ToPascalCase(col.Name)))
				}
				code.WriteString("); err != nil {\n")
				code.WriteString("\t\t\treturn nil, err\n")
				code.WriteString("\t\t}\n")
				code.WriteString("\t\titems = append(items, item)\n")
			} else {
				code.WriteString("\t\tvar item ")
				code.WriteString(g.mapColumnTypeToGo(columns[0].Type, columns[0].Nullable))
				code.WriteString("\n\t\tif err := rows.Scan(&item); err != nil {\n")
				code.WriteString("\t\t\treturn nil, err\n")
				code.WriteString("\t\t}\n")
				code.WriteString("\t\titems = append(items, item)\n")
			}
			code.WriteString("\t}\n")
			code.WriteString("\treturn items, rows.Err()\n")
		}

	case cmd == ":execresult":
		code.WriteString("\n\treturn q.db.Exec(ctx, query")
		if len(query.Params) > 0 {
			code.WriteString(", args...")
		}
		code.WriteString(")\n")

	default:
		code.WriteString("\n\t_, err := q.db.Exec(ctx, query")
		if len(query.Params) > 0 {
			code.WriteString(", args...")
		}
		code.WriteString(")\n")
		code.WriteString("\treturn err\n")
	}

	code.WriteString("}\n\n")

	if (cmd == ":one" || cmd == ":many") && len(columns) > 1 {
		code.WriteString(fmt.Sprintf("type %sRow struct {\n", methodName))
		for _, col := range columns {
			fieldName := utils.ToPascalCase(col.Name)
			goType := g.mapColumnTypeToGo(col.Type, col.Nullable)
			code.WriteString(fmt.Sprintf("\t%s %s `json:\"%s\"`\n", fieldName, goType, utils.ToSnakeCase(col.Name)))
		}
		code.WriteString("}\n\n")
	}

	return nil
}
