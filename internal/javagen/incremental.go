func (g *Generator) generateQueriesIncremental(queries []*parser.Query, fullRegen bool) error {
	queryGroups := make(map[string][]*parser.Query)
	for _, query := range queries {
		src := query.SourceFile
		if src == "" {
			src = "queries"
		}
		queryGroups[src] = append(queryGroups[src], query)
	}

	type fileGroup struct {
		src     string
		queries []*parser.Query
	}
	groups := make([]fileGroup, 0, len(queryGroups))
	for src, qs := range queryGroups {
		groups = append(groups, fileGroup{src, qs})
	}

	usedNames := make(map[string]int)
	var mu sync.Mutex

	numWorkers := runtime.NumCPU()
	if numWorkers > len(groups) {
		numWorkers = len(groups)
	}

	workCh := make(chan fileGroup, len(groups))
	errCh := make(chan error, len(groups))
	var wg sync.WaitGroup

	for i := 0; i < numWorkers; i++ {
		wg.Add(1)
		go func() {
			defer wg.Done()
			for fg := range workCh {
				errCh <- g.generateSingleJavaFile(fg.src, fg.queries, fullRegen, &mu, usedNames)
			}
		}()
	}
	for _, fg := range groups {
		workCh <- fg
	}
	close(workCh)
	go func() { wg.Wait(); close(errCh) }()
	for err := range errCh {
		if err != nil {
			return err
		}
	}
	return nil
}

func (g *Generator) generateSingleJavaFile(src string, queries []*parser.Query, fullRegen bool, mu *sync.Mutex, usedNames map[string]int) error {
	queryFile := filepath.Join(g.Config.Queries, src+".sql")
	currentHash, _ := gencommon.ComputeFileChecksum(queryFile)

	if !gencommon.ShouldRegenerateFile(g.cache, queryFile, currentHash, fullRegen) {
		gencommon.PrintSkipMessage(src, ".java")
		return nil
	}
	gencommon.PrintGenerateMessage(src, ".java")

	w := gencommon.GetBuilder()
	defer gencommon.PutBuilder(w)

	pkg := javaPackage(&g.Config.Gen.Java)
	w.WriteString("// Code generated by FlashORM. DO NOT EDIT.\n\n")
	w.WriteString(fmt.Sprintf("package %s;\n\n", pkg))

	needsUUID, needsLocalDateTime := false, false
	for _, q := range queries {
		for _, col := range q.Columns {
			jt := g.sqlTypeToJava(col.Type, false)
			if jt == "UUID" {
				needsUUID = true
			}
			if jt == "LocalDateTime" {
				needsLocalDateTime = true
			}
		}
		for _, param := range q.Params {
			jt := g.sqlTypeToJava(param.Type, false)
			if jt == "UUID" {
				needsUUID = true
			}
			if jt == "LocalDateTime" {
				needsLocalDateTime = true
			}
		}
	}
	if needsUUID {
		w.WriteString("import java.util.UUID;\n")
	}
	if needsLocalDateTime {
		w.WriteString("import java.time.LocalDateTime;\n")
	}

	driver := g.Config.Gen.Java.Driver
	provider := g.Config.Database.Provider
	isScylla := provider == "scylla" || provider == "scylladb" || provider == "cassandra"

	var connImport, connType, connParam string
	switch {
	case isScylla:
		connImport = "import com.datastax.oss.driver.api.core.CqlSession;\n"
		connType = "CqlSession"
		connParam = "session"
	case driver == "jooq":
		connImport = "import org.jooq.DSLContext;\n"
		connType = "DSLContext"
		connParam = "ctx"
	case driver == "hibernate":
		connImport = "import jakarta.persistence.EntityManager;\n"
		connType = "EntityManager"
		connParam = "em"
	default:
		connImport = "import java.sql.Connection;\n"
		connType = "Connection"
		connParam = "conn"
	}
	w.WriteString(connImport + "\n")

	// Emit each Row type as its own top-level .java file
	for _, q := range queries {
		columns := g.expandWildcardColumns(q)
		cmd := strings.ToLower(q.Cmd)
		rowType := gencommon.QueryPascal(q.Name) + "Row"
		if mt := g.modelTypeForQuery(q, columns); mt != "" {
			continue // uses existing model type, no Row file needed
		}
		if !((cmd == ":one" || cmd == ":many") && len(columns) > 1) {
			continue
		}
		var rw strings.Builder
		rw.WriteString("// Code generated by FlashORM. DO NOT EDIT.\n\n")
		rw.WriteString(fmt.Sprintf("package %s;\n\n", pkg))
		needsU, needsLDT := false, false
		for _, col := range columns {
			jt := g.sqlTypeToJava(col.Type, false)
			if jt == "UUID" {
				needsU = true
			}
			if jt == "LocalDateTime" {
				needsLDT = true
			}
		}
		if needsU {
			rw.WriteString("import java.util.UUID;\n")
		}
		if needsLDT {
			rw.WriteString("import java.time.LocalDateTime;\n")
		}
		rw.WriteString(fmt.Sprintf("\npublic record %s(\n", rowType))
		for i, col := range columns {
			sep := ","
			if i == len(columns)-1 {
				sep = ""
			}
			rw.WriteString(fmt.Sprintf("    %s %s%s\n", g.sqlTypeToJava(col.Type, col.Nullable), col.Name, sep))
		}
		rw.WriteString(") {}\n")
		rowFile := filepath.Join(g.Config.Gen.Java.Out, rowType+".java")
		if err := os.WriteFile(rowFile, []byte(rw.String()), 0644); err != nil {
			return err
		}
	}

	className := utils.ToPascalCase(strings.TrimSuffix(src, ".sql")) + "Queries"
	w.WriteString(fmt.Sprintf("public class %s {\n", className))
	w.WriteString(fmt.Sprintf("    private final %s %s;\n", connType, connParam))
	if !isScylla && driver != "jooq" && driver != "hibernate" {
		w.WriteString("    private final java.util.Map<String, java.sql.PreparedStatement> stmts = new java.util.HashMap<>();\n")
	}
	w.WriteString("\n")
	w.WriteString(fmt.Sprintf("    public %s(%s %s) { this.%s = %s; }\n\n", className, connType, connParam, connParam, connParam))

	if !isScylla && driver != "jooq" && driver != "hibernate" {
		w.WriteString("    public void close() throws java.sql.SQLException {\n")
		w.WriteString("        for (var s : stmts.values()) s.close();\n")
		w.WriteString("        stmts.clear();\n")
		w.WriteString("    }\n\n")
	}

	for _, q := range queries {
		g.generateQueryMethod(w, q)
	}
	w.WriteString("}\n")

	baseName := strings.TrimSuffix(src, ".sql")

	mu.Lock()
	outputFile := utils.ToPascalCase(baseName) + "Queries.java"
	if count, exists := usedNames[baseName]; exists {
		usedNames[baseName] = count + 1
		outputFile = fmt.Sprintf("%sQueries_%d.java", utils.ToPascalCase(baseName), count+1)
	} else {
		usedNames[baseName] = 1
	}
	mu.Unlock()

	path := filepath.Join(g.Config.Gen.Java.Out, outputFile)
	if err := os.WriteFile(path, []byte(w.String()), 0644); err != nil {
		return err
	}

	deps := gencommon.ExtractTableDependencies(queries)
	gencommon.UpdateCacheForFile(g.cache, queryFile, currentHash, deps, path)
	return nil
}
