type Generator struct {
	Config       *config.Config
	schema       *parser.Schema
	schemaParser *parser.SchemaParser
	queryParser  *parser.QueryParser
	cache        *gencommon.GenerationCache
	expander     *gencommon.SchemaExpander
}

func New(cfg *config.Config) *Generator {
	return &Generator{
		Config:       cfg,
		schemaParser: parser.NewSchemaParser(cfg),
		queryParser:  parser.NewQueryParser(cfg),
		cache:        gencommon.NewGenerationCacheWithDir(cfg.Gen.Python.Out),
	}
}

func (g *Generator) Generate() error {
	if err := os.MkdirAll(g.Config.Gen.Python.Out, 0755); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	schema, err := g.schemaParser.Parse()
	if err != nil {
		return fmt.Errorf("failed to parse schema: %w", err)
	}
	g.schema = schema
	g.expander = gencommon.NewSchemaExpander(schema)

	// Compute checksums for incremental generation
	schemaHash, _ := g.cache.ComputeSchemaChecksum(g.Config.SchemaDir)
	configStr := fmt.Sprintf("%s|%s|%v|%s", g.Config.Database.Provider, g.Config.Gen.Python.Out, g.Config.Gen.Python.Async, g.Config.Gen.Python.Driver)
	configHash := fmt.Sprintf("%x", []byte(configStr))
	fullRegen := g.cache.ShouldRegenerateAll(schemaHash, configHash)

	queries, err := g.queryParser.Parse(schema)
	if err != nil {
		return fmt.Errorf("failed to parse queries: %w", err)
	}

	if fullRegen || g.cache.SchemaChecksum != schemaHash {
		if err := g.generateModels(schema); err != nil {
			return err
		}
	}

	if err := g.generateQueriesIncremental(queries, fullRegen); err != nil {
		return err
	}

	if fullRegen || g.cache.SchemaChecksum != schemaHash {
		if err := g.generateDatabase(queries); err != nil {
			return err
		}

		if err := g.generateInit(); err != nil {
			return err
		}
	}

	// Always generate .pyi stub file for IDE autocomplete (needs all query info)
	if err := g.generateTypingStub(queries); err != nil {
		return err
	}

	g.cache.UpdateSchemaChecksum(schemaHash)
	g.cache.UpdateConfigChecksum(configHash)
	g.cache.MarkGeneration()
	_ = g.cache.Save()

	return nil
}

func (g *Generator) generateModels(schema *parser.Schema) error {
	var w strings.Builder
	// Pre-allocate: header + ~150 bytes per table
	estimatedSize := 200 + (len(schema.Tables) * 150)
	w.Grow(estimatedSize)
	w.WriteString("# Code generated by FlashORM. DO NOT EDIT.\n\n")
	w.WriteString("from dataclasses import dataclass\n")
	w.WriteString("from typing import Optional, List, Literal\n")
	w.WriteString("from datetime import datetime, timedelta\n")
	w.WriteString("from decimal import Decimal\n")

	// Add UUID import if any column uses UUID type
	needsUUID := false
	for _, table := range schema.Tables {
		for _, col := range table.Columns {
			if strings.Contains(strings.ToLower(col.Type), "uuid") {
				needsUUID = true
			}
		}
	}
	if needsUUID {
		w.WriteString("from uuid import UUID\n")
	}
	w.WriteString("\n")

	for _, table := range schema.Tables {
		w.WriteString("@dataclass\n")
		w.WriteString(fmt.Sprintf("class %s:\n", utils.Capitalize(table.Name)))
		for _, col := range table.Columns {
			pyType := g.sqlTypeToPython(col.Type, col.Nullable)
			w.WriteString(fmt.Sprintf("    %s: %s\n", col.Name, pyType))
		}
		w.WriteString("\n")
	}

	path := filepath.Join(g.Config.Gen.Python.Out, "models.py")
	return os.WriteFile(path, []byte(w.String()), 0644)
}

func (g *Generator) generateQueryMethod(w *strings.Builder, query *parser.Query) {
	methodName := utils.ToSnakeCase(query.Name)
	sql := g.convertSQL(query.SQL)
	sql = strings.ReplaceAll(sql, "\"", "\\\"")

	paramNames := make([]string, len(query.Params))
	paramTypes := make([]string, len(query.Params))
	for i, param := range query.Params {
		paramName := param.Name
		if paramName == "" {
			paramName = fmt.Sprintf("p%d", i+1)
		}
		paramNames[i] = paramName
		paramTypes[i] = fmt.Sprintf("%s: %s", paramName, g.sqlTypeToPython(param.Type, false))
	}

	returnType := g.getReturnType(query)

	// Build parameter string - avoid trailing comma when no params
	paramStr := ""
	if len(paramTypes) > 0 {
		paramStr = ", " + strings.Join(paramTypes, ", ")
	}

	// Generate async or sync method based on config
	if g.Config.Gen.Python.Async {
		w.WriteString(fmt.Sprintf("    async def %s(self%s) -> %s:\n",
			methodName, paramStr, returnType))
	} else {
		w.WriteString(fmt.Sprintf("    def %s(self%s) -> %s:\n",
			methodName, paramStr, returnType))
	}

	// Use statement caching for better performance
	w.WriteString(fmt.Sprintf("        _key = '%s'\n", methodName))
	w.WriteString("        if _key not in self._stmts:\n")
	w.WriteString(fmt.Sprintf("            self._stmts[_key] = \"\"\"%s\"\"\"\n", sql))
	w.WriteString("        stmt = self._stmts[_key]\n")

	provider := g.Config.Database.Provider
	driver := g.Config.Gen.Python.Driver
	switch provider {
	case "scylla", "scylladb", "cassandra":
		g.generateScyllaExecution(w, paramNames, query)
	case "sqlite", "sqlite3":
		if driver == "sqlite3" {
			g.generateSQLite3Execution(w, paramNames, query)
		} else {
			g.generateSQLiteExecution(w, paramNames, query)
		}
	case "mysql":
		if driver == "pymysql" {
			g.generatePyMySQLExecution(w, paramNames, query)
		} else {
			g.generateMySQLExecution(w, paramNames, query)
		}
	default:
		if driver == "psycopg3" {
			g.generatePsycopg3Execution(w, paramNames, query)
		} else {
			g.generatePostgreSQLExecution(w, paramNames, query)
		}
	}

	w.WriteString("\n")
}

func (g *Generator) generatePostgreSQLExecution(w *strings.Builder, paramNames []string, query *parser.Query) {
	hasColumns := len(query.Columns) > 0
	isSingleColumn := len(query.Columns) == 1
	isAsync := g.Config.Gen.Python.Async

	if query.Cmd == ":exec" {
		if isAsync {
			if len(paramNames) > 0 {
				w.WriteString(fmt.Sprintf("        await self.db.execute(stmt, %s)\n", strings.Join(paramNames, ", ")))
			} else {
				w.WriteString("        await self.db.execute(stmt)\n")
			}
		} else {
			if len(paramNames) > 0 {
				w.WriteString(fmt.Sprintf("        self.db.execute(stmt, (%s,))\n", strings.Join(paramNames, ", ")))
			} else {
				w.WriteString("        self.db.execute(stmt)\n")
			}
		}
		w.WriteString("        return 1\n")
		return
	}

	if isAsync {
		if len(paramNames) > 0 {
			w.WriteString(fmt.Sprintf("        result = await self.db.fetch(stmt, %s)\n", strings.Join(paramNames, ", ")))
		} else {
			w.WriteString("        result = await self.db.fetch(stmt)\n")
		}
	} else {
		if len(paramNames) > 0 {
			w.WriteString(fmt.Sprintf("        result = self.db.execute(stmt, (%s,)).fetchall()\n", strings.Join(paramNames, ", ")))
		} else {
			w.WriteString("        result = self.db.execute(stmt).fetchall()\n")
		}
	}

	isSingleNonWildcard := isSingleColumn && query.Columns[0].Name != "*"
	needsResultClass := g.needsResultClass(query)

	if hasColumns {
		if query.Cmd == ":one" {
			if isSingleNonWildcard {
				// Direct column access - fastest path
				w.WriteString(fmt.Sprintf("        return result[0]['%s'] if result else None\n", query.Columns[0].Name))
			} else if needsResultClass {
				className := utils.ToPascalCase(query.Name) + "Row"
				// asyncpg Records support key access directly - use _make_fast
				w.WriteString(fmt.Sprintf("        return %s._make_fast(result[0]) if result else None\n", className))
			} else {
				// Direct Record access for asyncpg (faster than dict())
				w.WriteString("        return result[0] if result else None\n")
			}
		} else {
			if isSingleNonWildcard {
				// Direct column access in list comprehension
				w.WriteString(fmt.Sprintf("        return [row['%s'] for row in result]\n", query.Columns[0].Name))
			} else if needsResultClass {
				className := utils.ToPascalCase(query.Name) + "Row"
				w.WriteString(fmt.Sprintf("        return [%s._make_fast(row) for row in result]\n", className))
			} else {
				// Return Records directly (asyncpg Records are dict-like)
				w.WriteString("        return list(result)\n")
			}
		}
	} else {
		switch query.Cmd {
		case ":one":
			w.WriteString("        return dict(result[0]) if result else None\n")
		case ":many":
			w.WriteString("        return [dict(row) for row in result]\n")
		default:
			w.WriteString("        return len(result)\n")
		}
	}
}

func (g *Generator) generateMySQLExecution(w *strings.Builder, paramNames []string, query *parser.Query) {
	returnType := utils.ToPascalCase(query.Name) + "Row"
	isSingleColumn := len(query.Columns) == 1
	isSingleNonWildcard := isSingleColumn && query.Columns[0].Name != "*"
	needsResultClass := g.needsResultClass(query)

	isAsync := g.Config.Gen.Python.Async

	if isAsync {
		w.WriteString("        async with self._get_conn() as conn:\n")
		w.WriteString("            async with conn.cursor() as cursor:\n")
	} else {
		w.WriteString("        with self.db.cursor() as cursor:\n")
	}

	indent := "            "
	if !isAsync {
		indent = "        "
	}

	if len(paramNames) > 0 {
		if isAsync {
			w.WriteString(fmt.Sprintf("                await cursor.execute(stmt, (%s,))\n", strings.Join(paramNames, ", ")))
		} else {
			w.WriteString(fmt.Sprintf("%s    cursor.execute(stmt, (%s,))\n", indent, strings.Join(paramNames, ", ")))
		}
	} else {
		if isAsync {
			w.WriteString("                await cursor.execute(stmt)\n")
		} else {
			w.WriteString(fmt.Sprintf("%s    cursor.execute(stmt)\n", indent))
		}
	}

	switch query.Cmd {
	case ":one":
		if isAsync {
			w.WriteString("                result = await cursor.fetchone()\n")
		} else {
			w.WriteString(fmt.Sprintf("%s    result = cursor.fetchone()\n", indent))
		}

		if isSingleNonWildcard {
			w.WriteString(fmt.Sprintf("%s    return result['%s'] if result else None\n", indent, query.Columns[0].Name))
		} else {
			w.WriteString(fmt.Sprintf("%s    if result:\n", indent))
			w.WriteString(fmt.Sprintf("%s        row_dict = dict(result) if hasattr(result, 'keys') else {desc[0]: result[i] for i, desc in enumerate(cursor.description)}\n", indent))
			if needsResultClass {
				w.WriteString(fmt.Sprintf("%s        return %s._make_fast(row_dict)\n", indent, returnType))
			} else {
				w.WriteString(fmt.Sprintf("%s        return row_dict\n", indent))
			}
			w.WriteString(fmt.Sprintf("%s    return None\n", indent))
		}
	case ":many":
		if isAsync {
			w.WriteString("                result = await cursor.fetchall()\n")
		} else {
			w.WriteString(fmt.Sprintf("%s    result = cursor.fetchall()\n", indent))
		}

		if isSingleNonWildcard {
			w.WriteString(fmt.Sprintf("%s    return [row['%s'] for row in result]\n", indent, query.Columns[0].Name))
		} else {
			w.WriteString(fmt.Sprintf("%s    rows = [dict(row) if hasattr(row, 'keys') else {cursor.description[i][0]: row[i] for i in range(len(row))} for row in result]\n", indent))
			if needsResultClass {
				w.WriteString(fmt.Sprintf("%s    return [%s._make_fast(row) for row in rows]\n", indent, returnType))
			} else {
				w.WriteString(fmt.Sprintf("%s    return rows\n", indent))
			}
		}
	case ":execresult":
		if isAsync {
			w.WriteString("                await conn.commit()\n")
			w.WriteString("                return cursor.lastrowid\n")
		} else {
			w.WriteString(fmt.Sprintf("%s    self.db.commit()\n", indent))
			w.WriteString(fmt.Sprintf("%s    return cursor.lastrowid\n", indent))
		}
	default:
		if isAsync {
			w.WriteString("                await conn.commit()\n")
			w.WriteString("                return cursor.rowcount\n")
		} else {
			w.WriteString(fmt.Sprintf("%s    self.db.commit()\n", indent))
			w.WriteString(fmt.Sprintf("%s    return cursor.rowcount\n", indent))
		}
	}
}

func (g *Generator) generateSQLiteExecution(w *strings.Builder, paramNames []string, query *parser.Query) {
	returnType := utils.ToPascalCase(query.Name) + "Row"
	isSingleColumn := len(query.Columns) == 1
	isSingleNonWildcard := isSingleColumn && query.Columns[0].Name != "*"
	needsResultClass := g.needsResultClass(query)
	isAsync := g.Config.Gen.Python.Async

	if isAsync {
		w.WriteString("        async with self.db.execute(stmt")
		if len(paramNames) > 0 {
			w.WriteString(fmt.Sprintf(", (%s,)", strings.Join(paramNames, ", ")))
		}
		w.WriteString(") as cursor:\n")
	} else {
		w.WriteString("        cursor = self.db.execute(stmt")
		if len(paramNames) > 0 {
			w.WriteString(fmt.Sprintf(", (%s,)", strings.Join(paramNames, ", ")))
		}
		w.WriteString(")\n")
	}

	indent := "            "
	if !isAsync {
		indent = "        "
	}

	switch query.Cmd {
	case ":one":
		if isAsync {
			w.WriteString("            result = await cursor.fetchone()\n")
		} else {
			w.WriteString("        result = cursor.fetchone()\n")
		}
		if isSingleNonWildcard {
			w.WriteString(fmt.Sprintf("%sreturn result['%s'] if result else None\n", indent, query.Columns[0].Name))
		} else {
			w.WriteString(fmt.Sprintf("%sif result:\n", indent))
			if needsResultClass {
				w.WriteString(fmt.Sprintf("%s    row_dict = {k: result[k] for k in result.keys()}\n", indent))
				w.WriteString(fmt.Sprintf("%s    return %s._make_fast(row_dict)\n", indent, returnType))
			} else {
				w.WriteString(fmt.Sprintf("%s    return {k: result[k] for k in result.keys()}\n", indent))
			}
			w.WriteString(fmt.Sprintf("%sreturn None\n", indent))
		}
	case ":many":
		if isAsync {
			w.WriteString("            result = await cursor.fetchall()\n")
		} else {
			w.WriteString("        result = cursor.fetchall()\n")
		}
		if isSingleNonWildcard {
			w.WriteString(fmt.Sprintf("%sreturn [row['%s'] for row in result]\n", indent, query.Columns[0].Name))
		} else if needsResultClass {
			w.WriteString(fmt.Sprintf("%sreturn [%s._make_fast({k: row[k] for k in row.keys()}) for row in result]\n", indent, returnType))
		} else {
			w.WriteString(fmt.Sprintf("%sreturn [{k: row[k] for k in row.keys()} for row in result]\n", indent))
		}
	default:
		w.WriteString(fmt.Sprintf("%sreturn cursor.rowcount\n", indent))
	}

	if !isAsync {
		w.WriteString("        cursor.close()\n")
	}
}

func (g *Generator) generateSQLite3Execution(w *strings.Builder, paramNames []string, query *parser.Query) {
	saved := g.Config.Gen.Python.Async
	defer func() { g.Config.Gen.Python.Async = saved }()
	g.Config.Gen.Python.Async = false
	g.generateSQLiteExecution(w, paramNames, query)
}

func (g *Generator) generatePyMySQLExecution(w *strings.Builder, paramNames []string, query *parser.Query) {
	saved := g.Config.Gen.Python.Async
	defer func() { g.Config.Gen.Python.Async = saved }()
	g.Config.Gen.Python.Async = false
	g.generateMySQLExecution(w, paramNames, query)
}

func (g *Generator) generateScyllaExecution(w *strings.Builder, paramNames []string, query *parser.Query) {
	isAsync := g.Config.Gen.Python.Async
	isSingleColumn := len(query.Columns) == 1 && query.Columns[0].Name != "*"
	hasColumns := len(query.Columns) > 0
	prefix := "await "
	if !isAsync {
		prefix = ""
	}

	if query.Cmd == ":exec" {
		if len(paramNames) > 0 {
			w.WriteString(fmt.Sprintf("        %sself.db.session.execute(stmt, (%s,))\n", prefix, strings.Join(paramNames, ", ")))
		} else {
			w.WriteString(fmt.Sprintf("        %sself.db.session.execute(stmt)\n", prefix))
		}
		w.WriteString("        return 1\n")
		return
	}

	if len(paramNames) > 0 {
		w.WriteString(fmt.Sprintf("        %sresult = self.db.session.execute(stmt, (%s,))\n", prefix, strings.Join(paramNames, ", ")))
	} else {
		w.WriteString(fmt.Sprintf("        %sresult = self.db.session.execute(stmt)\n", prefix))
	}

	switch query.Cmd {
	case ":one":
		if hasColumns {
			w.WriteString("        return result.one() if result else None\n")
		} else {
			w.WriteString("        return result.one() if result else None\n")
		}
	case ":many":
		if isSingleColumn && hasColumns {
			w.WriteString(fmt.Sprintf("        return [r.%s for r in result]\n", query.Columns[0].Name))
		} else {
			w.WriteString("        return list(result)\n")
		}
	default:
		w.WriteString("        return result\n")
	}
}

