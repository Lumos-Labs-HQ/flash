func (g *Generator) convertSQL(sql string) string {
	provider := g.Config.Database.Provider
	driver := g.Config.Gen.Python.Driver

	if driver == "psycopg3" {
		// psycopg3 uses %s placeholders
		for i := 20; i >= 1; i-- {
			sql = strings.ReplaceAll(sql, fmt.Sprintf("$%d", i), "%s")
		}
		return sql
	}

	switch provider {
	case "mysql":
		// MySQL uses %s placeholders for Python's cursor.execute
		for i := 20; i >= 1; i-- {
			sql = strings.ReplaceAll(sql, fmt.Sprintf("$%d", i), "%s")
		}
		placeholderCount := strings.Count(sql, "?")
		for i := 0; i < placeholderCount; i++ {
			sql = strings.Replace(sql, "?", "%s", 1)
		}
	case "sqlite", "sqlite3":
		// SQLite uses ? placeholders - convert PostgreSQL-style $1, $2 to ?
		for i := 20; i >= 1; i-- {
			sql = strings.ReplaceAll(sql, fmt.Sprintf("$%d", i), "?")
		}
	}
	// PostgreSQL uses $1, $2 style - no conversion needed
	return sql
}

func (g *Generator) sqlTypeToPython(sqlType string, nullable bool) string {
	sqlTypeLower := strings.ToLower(sqlType)
	var pyType string

	// Check for enum types
	if g.schema != nil {
		for _, enum := range g.schema.Enums {
			if strings.ToLower(enum.Name) == sqlTypeLower {
				quotedValues := make([]string, len(enum.Values))
				for i, v := range enum.Values {
					quotedValues[i] = fmt.Sprintf("'%s'", v)
				}
				pyType = fmt.Sprintf("Literal[%s]", strings.Join(quotedValues, ", "))
				if nullable {
					return fmt.Sprintf("Optional[%s]", pyType)
				}
				return pyType
			}
		}
	}

	switch {
	case strings.HasPrefix(sqlTypeLower, "enum("):
		values := parseMySQLEnumValues(sqlType)
		pyType = "Literal[" + strings.Join(values, ", ") + "]"
	case strings.Contains(sqlTypeLower, "[]"): // Array type
		if strings.Contains(sqlTypeLower, "int") {
			pyType = "List[int]"
		} else {
			pyType = "List[str]"
		}
	case strings.Contains(sqlTypeLower, "tinyint(1)"):
		pyType = "bool"
	case strings.Contains(sqlTypeLower, "bigint"), strings.Contains(sqlTypeLower, "int8"):
		pyType = "int"
	case strings.Contains(sqlTypeLower, "int"), strings.Contains(sqlTypeLower, "serial"), strings.Contains(sqlTypeLower, "smallint"):
		pyType = "int"
	case strings.Contains(sqlTypeLower, "numeric"), strings.Contains(sqlTypeLower, "decimal"):
		pyType = "Decimal"
	case strings.Contains(sqlTypeLower, "float"), strings.Contains(sqlTypeLower, "double"), strings.Contains(sqlTypeLower, "real"):
		pyType = "float"
	case strings.Contains(sqlTypeLower, "bool"):
		pyType = "bool"
	// CQL timeuuid must be checked before "time" — strings.Contains("timeuuid", "time") is true
	case sqlTypeLower == "timeuuid":
		pyType = "UUID"
	case strings.Contains(sqlTypeLower, "timestamp"), strings.Contains(sqlTypeLower, "date"):
		pyType = "datetime"
	case strings.Contains(sqlTypeLower, "time") && !strings.Contains(sqlTypeLower, "timestamp"):
		pyType = "datetime"
	case sqlTypeLower == "interval":
		pyType = "timedelta"
	case strings.Contains(sqlTypeLower, "json"):
		pyType = "dict"
	case strings.Contains(sqlTypeLower, "uuid"):
		pyType = "UUID"
	// ClickHouse-specific types
	case sqlTypeLower == "uint8", sqlTypeLower == "uint16", sqlTypeLower == "uint32", sqlTypeLower == "uint64",
		sqlTypeLower == "int16", sqlTypeLower == "int32", sqlTypeLower == "int64":
		pyType = "int"
	case sqlTypeLower == "float32", sqlTypeLower == "float64":
		pyType = "float"
	case sqlTypeLower == "string", strings.HasPrefix(sqlTypeLower, "fixedstring"):
		pyType = "str"
	case strings.HasPrefix(sqlTypeLower, "nullable("):
		inner := sqlTypeLower[9 : len(sqlTypeLower)-1]
		return g.sqlTypeToPython(inner, true)
	case strings.HasPrefix(sqlTypeLower, "array("):
		inner := sqlTypeLower[6 : len(sqlTypeLower)-1]
		elemType := g.sqlTypeToPython(inner, false)
		pyType = "List[" + elemType + "]"
	// ScyllaDB/Cassandra CQL types (non-uuid, checked after uuid above)
	case sqlTypeLower == "inet":
		pyType = "str"
	case sqlTypeLower == "varint", sqlTypeLower == "counter", sqlTypeLower == "duration":
		pyType = "int"
	case strings.HasPrefix(sqlTypeLower, "frozen<"), strings.HasPrefix(sqlTypeLower, "list<"),
		strings.HasPrefix(sqlTypeLower, "set<"), strings.HasPrefix(sqlTypeLower, "map<"),
		strings.HasPrefix(sqlTypeLower, "tuple<"):
		pyType = "Any"
	default:
		pyType = "str"
	}

	if nullable {
		return fmt.Sprintf("Optional[%s]", pyType)
	}
	return pyType
}

func (g *Generator) generateTypingStub(queries []*parser.Query) error {
	var w strings.Builder
	w.Grow(1024)

	w.WriteString("# Type stub for IDE autocomplete\n")
	w.WriteString("# Code generated by FlashORM. DO NOT EDIT.\n\n")
	w.WriteString("from typing import Any, Optional, List, Literal\n")
	w.WriteString("from datetime import datetime, timedelta\n")
	w.WriteString("from decimal import Decimal\n")

	// Add UUID import if any query uses UUID type
	needsUUID := false
	for _, query := range queries {
		for _, col := range query.Columns {
			if strings.Contains(strings.ToLower(col.Type), "uuid") {
				needsUUID = true
				break
			}
		}
		if needsUUID {
			break
		}
	}
	if needsUUID {
		w.WriteString("from uuid import UUID\n")
	}

	// Import all result classes from all query files
	queryGroups := make(map[string][]*parser.Query)
	for _, query := range queries {
		sourceFile := query.SourceFile
		if sourceFile == "" {
			sourceFile = "queries"
		}
		queryGroups[sourceFile] = append(queryGroups[sourceFile], query)
	}

	// Collect all result class names for imports - use map to avoid duplicates
	allResultClasses := make(map[string]bool)
	for sourceFile, fileQueries := range queryGroups {
		baseName := strings.TrimSuffix(sourceFile, ".sql")
		var resultClasses []string

		for _, query := range fileQueries {
			if g.needsResultClass(query) {
				// Match the actual generated class name format
				className := utils.ToPascalCase(query.Name) + "Row"
				resultClasses = append(resultClasses, className)
				allResultClasses[className] = true
			}
		}

		if len(resultClasses) > 0 {
			w.WriteString(fmt.Sprintf("from .%s import %s\n", baseName, strings.Join(resultClasses, ", ")))
		}
	}

	w.WriteString("\nclass Queries:\n")
	w.WriteString("    def __init__(self, db: Any) -> None: ...\n\n")

	// Generate method signatures for all queries
	seenMethods := make(map[string]bool)
	for _, query := range queries {
		methodName := utils.ToSnakeCase(query.Name)

		if seenMethods[methodName] {
			continue
		}
		seenMethods[methodName] = true

		// Build parameter list
		paramTypes := make([]string, len(query.Params))
		for i, param := range query.Params {
			paramName := param.Name
			if paramName == "" {
				paramName = fmt.Sprintf("p%d", i+1)
			}
			paramTypes[i] = fmt.Sprintf("%s: %s", paramName, g.sqlTypeToPython(param.Type, false))
		}

		// Get return type
		returnType := g.getReturnType(query)

		// Build parameter string - avoid trailing comma when no params
		paramStr := ""
		if len(paramTypes) > 0 {
			paramStr = ", " + strings.Join(paramTypes, ", ")
		}

		// Generate method signature
		if g.Config.Gen.Python.Async {
			w.WriteString(fmt.Sprintf("    async def %s(self%s) -> %s: ...\n",
				methodName, paramStr, returnType))
		} else {
			w.WriteString(fmt.Sprintf("    def %s(self%s) -> %s: ...\n",
				methodName, paramStr, returnType))
		}
	}

	// Add new() function signature
	w.WriteString("\ndef newq(db: Any) -> Queries: ...\n")

	path := filepath.Join(g.Config.Gen.Python.Out, "database.pyi")
	return os.WriteFile(path, []byte(w.String()), 0644)
}

func (g *Generator) needsResultClass(query *parser.Query) bool {
	if len(query.Columns) == 0 {
		return false
	}

	if len(query.Columns) == 1 && query.Columns[0].Name != "*" {
		return false
	}

	return true
}

func (g *Generator) generateResultClass(w *strings.Builder, query *parser.Query) {
	className := utils.ToPascalCase(query.Name) + "Row"

	columns := g.expandWildcardColumns(query)
	if len(columns) == 0 {
		return
	}

	// Filter out wildcard columns (e.g. u.* in SELECT u.*, published_posts FROM ...)
	realCols := make([]*parser.QueryColumn, 0, len(columns))
	for _, col := range columns {
		if col.Name != "*" && !strings.HasPrefix(col.Name, "/*") {
			realCols = append(realCols, col)
		}
	}
	if len(realCols) == 0 {
		return
	}

	w.WriteString("@dataclass\n")
	w.WriteString(fmt.Sprintf("class %s:\n", className))
	for _, col := range realCols {
		pyType := g.sqlTypeToPython(col.Type, col.Nullable)
		w.WriteString(fmt.Sprintf("    %s: %s\n", utils.ToSnakeCase(col.Name), pyType))
	}

	// Add optimized factory method for faster Record -> dataclass conversion
	w.WriteString("\n")
	w.WriteString("    @classmethod\n")
	w.WriteString("    def _make_fast(cls, record):\n")
	w.WriteString("        return cls(\n")
	for i, col := range realCols {
		colName := utils.ToSnakeCase(col.Name)
		if i < len(realCols)-1 {
			w.WriteString(fmt.Sprintf("            record['%s'],\n", colName))
		} else {
			w.WriteString(fmt.Sprintf("            record['%s']\n", colName))
		}
	}
	w.WriteString("        )\n")
	w.WriteString("\n")
}

func (g *Generator) expandWildcardColumns(query *parser.Query) []*parser.QueryColumn {
	if g.expander == nil {
		g.expander = gencommon.NewSchemaExpander(g.schema)
	}
	return g.expander.ExpandWildcardColumns(query)
}

func (g *Generator) getReturnType(query *parser.Query) string {
	hasColumns := len(query.Columns) > 0
	isSingleColumn := len(query.Columns) == 1 && query.Columns[0].Name != "*"

	if hasColumns && isSingleColumn {
		pyType := g.sqlTypeToPython(query.Columns[0].Type, false)
		if query.Cmd == ":one" {
			return fmt.Sprintf("Optional[%s]", pyType)
		}
		return fmt.Sprintf("List[%s]", pyType)
	}

	if g.needsResultClass(query) {
		// Match the actual generated class name format: ToPascalCase(query.Name) + "Row"
		className := utils.ToPascalCase(query.Name) + "Row"
		if query.Cmd == ":one" {
			return fmt.Sprintf("Optional[%s]", className)
		}
		return fmt.Sprintf("List[%s]", className)
	}

	switch query.Cmd {
	case ":one":
		return "Optional[dict]"
	case ":many":
		return "List[dict]"
	}
	return "int"
}

// parseMySQLEnumValues extracts quoted values from MySQL ENUM('a', 'b') syntax
func parseMySQLEnumValues(sqlType string) []string {
	start := strings.Index(sqlType, "(")
	end := strings.LastIndex(sqlType, ")")
	if start == -1 || end == -1 || end <= start {
		return nil
	}
	inner := sqlType[start+1 : end]
	var values []string
	var current strings.Builder
	inQuote := false
	quoteChar := rune(0)
	for _, ch := range inner {
		if !inQuote && (ch == '\'' || ch == '"') {
			inQuote = true
			quoteChar = ch
			current.Reset()
		} else if inQuote && ch == quoteChar {
			inQuote = false
			values = append(values, fmt.Sprintf("'%s'", current.String()))
			current.Reset()
		} else if inQuote {
			current.WriteRune(ch)
		}
	}
	return values
}
